const {resolve, join} = require('path');
const publicPath = resolve(__dirname, '../../src');
const getOutputPath = path => join(publicPath, path);
const PUBLIC_GEN_TIP = '/* generated by \'bin/codeGen/templateGenerate.js\' */';

function getMethodTemplate(fncName) {
    return `${PUBLIC_GEN_TIP}

export function ${fncName}() {

}
`;
}

function getTestTempate(fncName) {
    return `${PUBLIC_GEN_TIP}
import {${fncName}} from '../index';

describe('${fncName} is ok', () => {
    it('', () => {
        
    });
});
`;
}

exports.getFileDirMaps = fncName => ([
    {
        path: getOutputPath(fncName + '/index.ts'),
        template: getMethodTemplate(fncName)
    },
    {
        path: getOutputPath(fncName + '/index.test.ts'),
        template: getTestTempate(fncName)
    }
]);

